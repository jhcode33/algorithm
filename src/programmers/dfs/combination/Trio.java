package programmers.dfs.combination;

//문제 설명
//한국중학교에 다니는 학생들은 각자 정수 번호를 갖고 있습니다. 이 학교 학생 3명의 정수 번호를 더했을 때 0이 되면 3명의 학생은 삼총사라고 합니다.
//예를 들어, 5명의 학생이 있고, 각각의 정수 번호가 순서대로 -2, 3, 0, 2, -5일 때, 첫 번째, 세 번째, 네 번째 학생의 정수 번호를 더하면 0이므로 세 학생은 삼총사입니다.
//또한, 두 번째, 네 번째, 다섯 번째 학생의 정수 번호를 더해도 0이므로 세 학생도 삼총사입니다.
//따라서 이 경우 한국중학교에서는 두 가지 방법으로 삼총사를 만들 수 있습니다.
//한국중학교 학생들의 번호를 나타내는 정수 배열 number가 매개변수로 주어질 때,
//학생들 중 삼총사를 만들 수 있는 방법의 수를 return 하도록 solution 함수를 완성하세요.
//
//제한사항
//3 ≤ number의 길이 ≤ 13
//-1,000 ≤ number의 각 원소 ≤ 1,000
//서로 다른 학생의 정수 번호가 같을 수 있습니다.
//입출력 예
//number	result
//[-2, 3, 0, 2, -5]	        2
//[-3, -2, -1, 0, 1, 2, 3]	5
//[-1, 1, -1, 1]	        0
//
//입출력 예 #2
//학생들의 정수 번호 쌍 (-3, 0, 3), (-2, 0, 2), (-1, 0, 1), (-2, -1, 3), (-3, 1, 2) 이 삼총사가 될 수 있으므로, 5를 return 합니다.
//
//입출력 예 #3
//삼총사가 될 수 있는 방법이 없습니다.

// 경우의 수 - 조합 탐색형 DFS
// DFS란 모든 조합 경로 탐색이다.
// 트리 형태의 모든 경로를 끝까지 탐색하는 알고리즘.
// 매 인덱스마다 두가지 선택지가 생긴다. 현재 학생을 삼총사에 포함할 것인가? 아니면 포함하지 않을 것인가?
public class Trio {
    public int solution(int[] number) {
        return dfs(number, 0, 0, 0);
    }

    private int dfs(int[] a, int idx, int pick, int sum) {
        // 정확히 3개를 골랐을 경우: 합이 0이면 1가지 방법 <-> 아니면 0
        if (pick == 3) return (sum == 0) ? 1 : 0;

        // 더 고를 수 없는 경우 (배열 끝, 종료)
        if (idx == a.length) return 0;

        int count = 0;

        // [선택 분기] a[idx]를 고르는 경우
        count += dfs(a, idx + 1, pick + 1, sum + a[idx]);

        // [비선택 분기] a[idx]를 고르지 않는 경우
        count += dfs(a, idx + 1, pick, sum);

        return count;
    }
}
